#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const pkgRoot = path.resolve(__dirname, '..');
const whitelistPath = path.join(pkgRoot, 'mangled-whitelist.json');
const outPath = path.join(pkgRoot, 'src', 'mangled-names.generated.ts');

function ensureExampleWhitelist() {
  if (!fs.existsSync(whitelistPath)) {
    console.log('Creating example mangled-whitelist.json...');
    const example = [
      'distinct_id',
      'properties',
      'event',
      'timestamp'
    ];
    fs.writeFileSync(whitelistPath, JSON.stringify(example, null, 2));
  }
}

function readWhitelist() {
  const raw = fs.readFileSync(whitelistPath, 'utf8');
  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch (e) {
    console.error('Unable to parse mangled-whitelist.json');
    process.exit(1);
  }
  if (!Array.isArray(parsed)) {
    console.error('mangled-whitelist.json must be an array of strings');
    process.exit(1);
  }
  return [...new Set(parsed.map(String))].sort();
}

const alphabet = 'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
const base = alphabet.length;
function encode(n) {
  if (n === 0) return alphabet[0];
  let s = '';
  while (n > 0) {
    s = alphabet[n % base] + s;
    n = Math.floor(n / base);
  }
  return s;
}

function buildMapping(keys) {
  const mapping = {};
  for (let i = 0; i < keys.length; i++) mapping[keys[i]] = encode(i);
  return mapping;
}

function writeGenerated(mapping) {
  const content = `// Auto-generated by scripts/write-mangled-property-names.mjs\n// DO NOT EDIT — generated from mangled-whitelist.json\nexport default ${JSON.stringify(mapping, null, 2)};\n`;
  fs.writeFileSync(outPath, content, 'utf8');
  console.log(`Wrote ${path.relative(pkgRoot, outPath)} (${Object.keys(mapping).length} keys)`);
}

// Run
ensureExampleWhitelist();
const keys = readWhitelist();
const mapping = buildMapping(keys);
writeGenerated(mapping);
